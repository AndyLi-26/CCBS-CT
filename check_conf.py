def conv(s):
    try:
        return int(s)
    except:
        return round(float(s),8)
def f2conf(fn):
    retval=set()
    with open(fn) as f:
        for l in f:
            l=l.strip().split(",")
            idx=tuple(map(conv,l))
            if idx in retval:
                pass
            else:
                retval.add(idx)
    return retval
    
